$(function() {

    crossover();

///////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////NAVBAR ANIMATION//////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////
    $(window).scroll(function() {
        var scroll = $(window).scrollTop();
        if (scroll > 65) {
            $('nav, .navbar-brand, .nav-link, .active-link').addClass('scrolled');
        } else {
            $('nav, .navbar-brand, .nav-link, .active-link').removeClass('scrolled');
        }
    });

});